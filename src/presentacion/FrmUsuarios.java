    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        //Este codigo es para validar el ingreso en los respectivos controles y luego realizar la insercion
        if (txtUsuario.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Nombre para el Usuario");
            txtUsuario.requestFocus();
            return;
        }
        if (txtLogin.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Login para el Usuario");
            txtLogin.requestFocus();
            return;
        }
        if (txtpContrasenia.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una Contraseña para el Usuario");
            txtpContrasenia.requestFocus();
            return;
        }
        
        if(txtpContraseniaRepetida.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes repetir una contraseña para el Usuario");
            txtpContraseniaRepetida.requestFocus();
            return;
        }
        /*Comparacion entre las contraseñas, estan deben coincidir*/
        if(!(txtpContrasenia.getText().equals(txtpContraseniaRepetida.getText()))){
            JOptionPane.showConfirmDialog(rootPane, "Las contraseñas no coinciden");
            txtpContraseniaRepetida.requestFocus();
            return;
        }

        Lusuarios dts = new Lusuarios();
        Fusuarios func = new Fusuarios();
        String passEncriptado;
        /*Si el atributo accion es editar no sera necesario encriptar nuevamente la cadena, solo se volvera
          a almacenar la misma cadena*/
        if (accion.equals("editar")) {
            if (accionCambio.equals("cambio")) {
                passEncriptado = DigestUtils.md5Hex(txtpContrasenia.getText());
                accionCambio = "nocambio";
            } else {
                passEncriptado = txtpContrasenia.getText();
            }
        } else {
            passEncriptado = DigestUtils.md5Hex(txtpContrasenia.getText());
        }

        dts.setUsuario(txtUsuario.getText());
        dts.setFono(txtFono.getText());
        dts.setLogin(txtLogin.getText());
        dts.setContrasenia(passEncriptado);
        int seleccionado = cboPerfil.getSelectedIndex();
        dts.setPerfil((String) cboPerfil.getItemAt(seleccionado));

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El Usuario fue registrado satisfactoriamente");
                muestra("");
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
            dts.setIdusuario(Integer.parseInt(txtIdUsuario.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El Usuario fue editado satisfactoriamente");
                muestra("");
                inhabilitar();
            }
        }
        btnNuevo.setEnabled(true);

    }//GEN-LAST:event_btnGuardarActionPerformed

