    private void tblListadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListadoMouseClicked
        //
        btnGuardar.setText("Editar");
        habilitar();
        btnNuevo.setEnabled(false);
        btnEliminar.setEnabled(true);
        accion = "editar";

        int fila = tblListado.rowAtPoint(evt.getPoint());

        txtIdUsuario.setText(tblListado.getValueAt(fila, 0).toString());
        txtUsuario.setText(tblListado.getValueAt(fila, 1).toString());
        txtFono.setText(tblListado.getValueAt(fila, 2).toString());
        txtLogin.setText(tblListado.getValueAt(fila, 3).toString());
        txtpContrasenia.setText(tblListado.getValueAt(fila, 4).toString());
        txtpContraseniaRepetida.setText(tblListado.getValueAt(fila, 4).toString());
        //
        txtpContrasenia.setEnabled(false);
        txtpContraseniaRepetida.setEnabled(false);
        //
        cboPerfil.setSelectedItem(tblListado.getValueAt(fila, 5).toString());
        
        txtUsuario.requestFocus();

    }//GEN-LAST:event_tblListadoMouseClicked

