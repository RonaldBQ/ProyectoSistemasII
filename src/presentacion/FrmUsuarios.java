    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //
        if(!txtIdUsuario.getText().equals("")){
            int confirmacion=JOptionPane.showConfirmDialog(rootPane, "Estas seguro de eliminar el Usuario seleccionado?","Confirmar",2);
            if(confirmacion==0){
                Fusuarios func=new Fusuarios();
                Lusuarios dts=new Lusuarios();
                
                dts.setIdusuario(Integer.parseInt(txtIdUsuario.getText()));
                func.eliminar(dts);
                muestra("");
                inhabilitar();
            }
        }
       btnNuevo.setEnabled(true);
    }//GEN-LAST:event_btnEliminarActionPerformed

